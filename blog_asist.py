import streamlit as st
import google.generativeai as genai 

st.set_page_config(page_title="블로그 자동 포스팅 봇", page_icon="✍️", layout="centered")

st.title("✍️ 방문요양센터 맞춤형 블로그 원고 자판기")
st.markdown("어떤 주제든 척척! 10년 차 센터장의 따뜻한 톤앤매너로 네이버 최적화 원고를 뽑아냅니다.")

# 입력창 구성 (주제와 키워드)
topic_input = st.text_input("💡 오늘 포스팅할 [주제]를 입력하세요", placeholder="예: 치매 부모님 요양원 대신 방문요양을 선택해야 하는 3가지 이유")
keyword_input = st.text_input("🔑 네이버 검색에 걸릴 [핵심 키워드] 3개를 쉼표로 구분해 적어주세요", placeholder="예: 치매방문요양, 요양보호사, 가족요양")

if st.button("🚀 네이버 블로그 원고 작성 시작!", use_container_width=True):
    if topic_input == "" or keyword_input == "":
        st.warning("주제와 키워드를 모두 입력해 주세요!")
    else:
        with st.spinner("센터장님 빙의 중... 입력하신 주제에 맞춰 정성스러운 원고를 작성하고 있습니다. (약 10~20초 소요)"):
            try:
                # 보안 키 불러오기
                api_key = st.secrets["GEMINI_API_KEY"]
                genai.configure(api_key=api_key)
                
                # ★ 범용성(다양한 주제)을 확보하도록 전면 수정된 마스터 프롬프트
                system_prompt = f"""
                너는 10년 이상의 현장 경력을 가진 따뜻하고 전문적인 [방문요양센터]의 센터장이야. 
                수많은 어르신을 케어해왔고, 막막해하는 보호자(자녀)들의 고충을 누구보다 잘 이해하고 있어.
                아래의 [주제]와 [핵심 키워드]를 바탕으로, 네이버 블로그 검색 상위 노출(SEO)에 최적화된 정보성 칼럼을 작성해 줘.

                [주제]: {topic_input}
                [핵심 키워드]: {keyword_input}

                [블로그 작성 절대 규칙 - 반드시 준수할 것]
                1. 주제 완벽 맞춤 (범용성): 사용자가 입력한 [주제]에 100% 초점을 맞춰서 글을 전개할 것. 치매 케어, 등급 신청, 일상 돌봄, 센터 후기 등 어떤 주제가 들어오든 그에 맞는 전문 지식과 노하우를 자연스럽게 풀어내야 함.
                2. 톤앤매너: 눈앞에서 보호자에게 직접 상담해 주는 듯한 따뜻한 '해요체(~해요, ~습니다, ~할까요?)'를 사용할 것. 
                3. 맞춤형 상황 공감 (Hook): 서론에서는 무조건 뻔한 위로를 하는 게 아니라, 주어진 [주제]와 관련된 보호자의 '구체적인 고충'을 짚어주며 깊이 공감할 것. (예: 치매 주제라면 반복되는 질문에 지친 마음, 요양보호사 매칭 주제라면 낯선 사람을 집에 들이는 걱정 등)
                4. SEO 및 키워드 배치: [핵심 키워드]는 글 제목, 본문 첫 문단, 중간 소제목(##), 마지막 결론에 억지로 끼워 넣은 티가 나지 않게 문맥에 맞춰 1~2회씩만 배치할 것.
                5. 가독성 및 시각화: 스마트폰 가독성을 위해 한 문단은 3줄을 넘지 않게 할 것. 본문 내용 흐름에 맞춰 사진이 들어갈 자리에 [📷 (내용에 맞는 사진 설명) 사진 삽입] 형식으로 플레이스홀더를 3곳 이상 넣어줄 것.
                6. 자연스러운 행동 유도 (CTA): 결론에서는 작성한 [주제]와 관련하여 우리 센터가 어떤 도움을 줄 수 있는지(상담, 대행, 맞춤 매칭 등) 자연스럽게 연결할 것. "더 궁금한 점이 있으시거나 도움이 필요하시다면 편하게 연락 주세요"라는 따뜻한 멘트와 함께 [☎️ 000-0000-0000] 빈칸을 포함할 것.

                [작성 지침]
                - 딱딱한 '서론-본론-결론' 양식을 탈피하여, 실제 센터장이 보호자의 질문에 하나씩 대답해 주는 형식의 자연스러운 스토리텔링으로 작성할 것.
                - 소제목을 적극 활용하여 독자가 필요한 정보만 빠르게 스캐닝할 수 있게 구성해 줘.
                - 글 중간에 Q&A 형식 (Q. "보호자의 현실적인 질문" / A. "센터장의 명쾌한 답변")을 1~2회 넣어 글을 풍성하게 만들 것. 이모티콘(😊, 🌿, 💡 등)은 과하지 않게 적절히 섞어줄 것.
                - 글의 맨 마지막에는 검색 유입을 위한 관련 해시태그(#) 10개를 달아줄 것(중랑방문요양 이란 키워드는 꼭 포함).
                """
                
                model = genai.GenerativeModel(
                    model_name="gemini-2.5-flash",
                    system_instruction=system_prompt
                )
                
                generation_config = genai.types.GenerationConfig(temperature=0.7)
                
                response = model.generate_content(
                    "위의 지시사항과 주어진 주제, 키워드에 맞춰서 완벽한 블로그 원고를 작성해 주세요.", 
                    generation_config=generation_config
                )
                
                st.success("🎉 완벽한 블로그 원고가 완성되었습니다! 내용을 복사해서 네이버 블로그에 붙여넣으세요.")
                
                # 결과물을 복사하기 쉽게 깔끔한 박스 안에 출력
                st.container(border=True)
                st.markdown(response.text)

            except Exception as e:
                st.error(f"에러발생: {e}")
